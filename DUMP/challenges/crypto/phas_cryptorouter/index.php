<p class="challenge_1" style="text-align:left;">Crypto Router</p>
<span style="color:#F0F000;">This is the information we have about the communication system of the enemy:</span><br />
<table cellspacing="0" class="list">
<tr><td class="a">-</td><td class="b">They have four corps (Catering Corps, Munitions Corps, Supply Corps &amp; Signal Corps) that can transmit messages.</td></tr>
<tr><td class="a">-</td><td class="b">They have four Divisions (North, East, South &amp; West Division) that can receive messages.</td></tr> 
<tr><td class="a">-</td><td class="b">Each Corp uses a different encryption system and key.</td></tr> 
<tr><td class="a">-</td><td class="b">Each Division uses a different encryption system and key.</td></tr>
<tr><td class="a">-</td><td class="b">They have a kind of Crypto-Router that receives a message from any Corp, decrypts it, determines to which Division must be sent, encrypts the message with the encryption system and key corresponding to the destination Division and sends the message to that Division.</td></tr> 
<tr><td class="a">-</td><td class="b">We suspect they are only using a total of four encryption systems, but the keys are different when used in a Corp or in a Division.</td></tr> 
<tr><td class="a">-</td><td class="b">We can not intercept messages near the Corps or near the Divisions, but we have intercepted some messages (see the list below) near the Crypto-Router.</td></tr> 
<tr><td class="a">-</td><td class="b">We could not get the message headers, so we don't know the sender or the recipient, but we have divided the messages in two groups incoming (messages that arrive to the Crypto-Router from a Corp) and outgoing (messages sent by the Crypto-Router to a Division).</td></tr> 
<tr><td class="a">-</td><td class="b">We have also intercepted a message being received in the East Division. We suspect this is the most important message. Can you read it?</td></tr>
